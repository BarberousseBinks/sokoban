    public boolean movePlayer(int x, int y){  //Fait avancer le joueur du vecteur (x,y)
        if(abs(x)+abs(y) != 1){
            throw new IllegalArgumentException("movePlayer(int x,int y) only takes [(0,1),(0,-1),(1,0)or(-1,0)] as parameters (x,y)"); 
        }     //Il n'est pas ncessaire de vrifier si on tombe dans un cas outOfRange car le joueur ne sera jamais en position limite car le plateau est bord de murs
              //Par exemple il ne sera jamais ncessaire de vrifier que si x=-1 alors pX ne doit pas valoir 0 pour viter l'outOfRange car pX ne vaudra jamais 0)
        
        y=-y; //Pour faire avancer de (x,y) dans un repre conventiel, il faut faire avancer de (x,-y) dans le repre droulant vers le bas du board ((0,0) est en haut  gauche)
        
        if("player".equals(tab.get(pY).get(pX).getType())){ //Le joueur ne se trouve pas sur un objectif
            return movePlayerFromEmpty(x,y);
        }
        else{
            return movePlayerFromGoal(x,y);
        }
    }
    
